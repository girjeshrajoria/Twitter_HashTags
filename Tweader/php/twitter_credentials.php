/*

get the keys from https://apps.twitter.com

*/

function getTwitterCredentials()
{

    $settings = array(
        'oauth_access_token' => "",
        'oauth_access_token_secret' => "",
        'consumer_key' => "",
        'consumer_secret' => ""
    );

    return $settings;

}
